function multiplyBy()
{
  num1 = document.getElementById("Resistance").value;
  num2 = document.getElementById("Capacitance").value;
  document.getElementById("result").innerHTML = num1 * num2;
}

function divideBy()
{
  num1 = document.getElementById("Resistance").value;
  num2 = document.getElementById("Capacitance").value;
  document.getElementById("result").innerHTML = num1 / num2;
}
